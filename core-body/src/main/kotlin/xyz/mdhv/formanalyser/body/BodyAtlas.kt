    /** All 52 regions: authored centerline + left, plus mirrored right. */
    val regions: List<RegionShape> = buildList {
        for (a in authored) {
            add(RegionShape(a.id, a.face, a.x, a.y, a.w, a.h))
            if (a.id.endsWith("_l")) {
                val rid = a.id.dropLast(2) + "_r"
                add(RegionShape(rid, a.face, WIDTH - a.x - a.w, a.y, a.w, a.h))
            }
        }
    }

    val byId: Map<String, RegionShape> = regions.associateBy { it.id }

    fun forFace(face: BodyFace): List<RegionShape> = regions.filter { it.face == face }

    /** First (only, by the no-overlap invariant) region containing the point, or null. */
    fun hitTest(face: BodyFace, x: Double, y: Double): RegionShape? =
        forFace(face).firstOrNull { it.contains(x, y) }
}
