package xyz.mdhv.formanalyser.body

/**
 * Schematic atlas geometry for the 52-region contract (Phase 3 §A). Viewport is 1000×2000 units
 * per side (FRONT/BACK), body centered, head at top.
 *
 * v0 geometry is deliberately simple: each region is an axis-aligned rounded rectangle. That is
 * enough to satisfy every integrity requirement (all 52 present, exact left/right mirrors,
 * finger-sized ≥60×60 boxes, zero overlaps, centroid hit-tests) while staying pure JVM and
 * headless-testable. Hand-drawn SVG paths can replace this later via the
 * `body_atlas.override.json` drop-in seam without touching IDs or consumers.
 *
 * Author-once-mirror-right: only left-side and centerline rects are authored; right-side rects are
 * generated by `x' = WIDTH − x − w`, so symmetry is guaranteed, not hoped for.
 */
data class RegionShape(
    val id: String,
    val face: BodyFace,
    val x: Double,
    val y: Double,
    val w: Double,
    val h: Double,
) {
    val centerX: Double get() = x + w / 2
    val centerY: Double get() = y + h / 2
    fun contains(px: Double, py: Double): Boolean = px >= x && px < x + w && py >= y && py < y + h
    fun overlaps(o: RegionShape): Boolean =
        face == o.face && x < o.x + o.w && o.x < x + w && y < o.y + o.h && o.y < y + h
}

